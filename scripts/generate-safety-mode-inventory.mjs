#!/usr/bin/env node
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, basename } from 'node:path';

const ROOT = process.cwd();
const CLI_DIR = join(ROOT, 'packages/cli/src/commands');
const MCP_DIR = join(ROOT, 'packages/mcp/src/tools');
const OUTPUT = join(ROOT, 'docs/safety-mode-inventory.md');

const MODE_LABEL = {
  READ_ONLY_DEFAULT: 'read-only',
  WRITE_IDEMPOTENT: 'write-idempotent',
  WRITE_DESTRUCTIVE: 'write-destructive',
};

function titleCaseFromFile(file) {
  return file
    .replace(/\.ts$/, '')
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function getCliRows() {
  const files = readdirSync(CLI_DIR)
    .filter((f) => f.endsWith('.ts'))
    .sort();

  const rows = [];

  for (const file of files) {
    const src = readFileSync(join(CLI_DIR, file), 'utf8');
    const commandRegex = /\.command\('([^']+)'/g;
    const wrapRegex = /wrapAction\(\s*(READ_ONLY_DEFAULT|WRITE_IDEMPOTENT|WRITE_DESTRUCTIVE)/g;

    const commands = [];
    let commandMatch;
    while ((commandMatch = commandRegex.exec(src)) !== null) {
      commands.push({ index: commandMatch.index, command: commandMatch[1] });
    }

    let wrapMatch;
    while ((wrapMatch = wrapRegex.exec(src)) !== null) {
      const index = wrapMatch.index;
      const modeKey = wrapMatch[1];
      let nearestCommand = '(command not parsed)';
      for (const command of commands) {
        if (command.index < index) nearestCommand = command.command;
        else break;
      }
      rows.push({
        module: file,
        command: nearestCommand,
        mode: MODE_LABEL[modeKey],
      });
    }
  }

  return rows.sort((a, b) => {
    if (a.module !== b.module) return a.module.localeCompare(b.module);
    return a.command.localeCompare(b.command);
  });
}

function getMcpRows() {
  const files = readdirSync(MCP_DIR)
    .filter((f) => f.endsWith('.ts') && !['index.ts', 'shared.ts', 'shared.test.ts'].includes(f))
    .sort();

  const rows = [];

  for (const file of files) {
    const src = readFileSync(join(MCP_DIR, file), 'utf8');
    const reg = /registerToolSafe\(\s*server,\s*'([^']+)',\s*\{[\s\S]*?annotations:\s*(READ_ONLY_DEFAULT|WRITE_IDEMPOTENT|WRITE_DESTRUCTIVE)/g;

    let match;
    while ((match = reg.exec(src)) !== null) {
      rows.push({
        module: file,
        tool: match[1],
        mode: MODE_LABEL[match[2]],
      });
    }
  }

  return rows.sort((a, b) => {
    if (a.module !== b.module) return a.module.localeCompare(b.module);
    return a.tool.localeCompare(b.tool);
  });
}

function uniqueModes(items, key) {
  const grouped = new Map();
  for (const item of items) {
    const k = item[key];
    if (!grouped.has(k)) grouped.set(k, new Set());
    grouped.get(k).add(item.mode);
  }
  return [...grouped.entries()].map(([name, modes]) => ({
    name,
    modes: [...modes].sort().join(', '),
  }));
}

const cliRows = getCliRows();
const mcpRows = getMcpRows();

const domains = new Set([
  ...cliRows.map((r) => r.module.replace(/\.ts$/, '')),
  ...mcpRows.map((r) => r.module.replace(/\.ts$/, '')),
]);

const domainRows = [...domains]
  .sort()
  .map((domain) => {
    const cliModules = new Set(
      cliRows.filter((r) => r.module === `${domain}.ts`).map((r) => `\`${r.module}\``),
    );
    const mcpModules = new Set(
      mcpRows.filter((r) => r.module === `${domain}.ts`).map((r) => `\`${r.module}\``),
    );

    return `| ${titleCaseFromFile(domain)} | ${
      cliModules.size ? [...cliModules].join(', ') : '(n/a)'
    } | ${mcpModules.size ? [...mcpModules].join(', ') : '(n/a)'} |`;
  })
  .join('\n');

const cliCoverageRows = uniqueModes(cliRows, 'module')
  .map((row) => `| \`${row.name}\` | ${row.modes} |`)
  .join('\n');

const cliCommandRows = cliRows
  .map((row) => `| \`${row.module}\` | \`${row.command}\` | ${row.mode} |`)
  .join('\n');

const mcpCoverageRows = uniqueModes(mcpRows, 'module')
  .map((row) => `| \`${row.name}\` | ${row.modes} |`)
  .join('\n');

const mcpToolRows = mcpRows
  .map((row) => `| \`${row.module}\` | \`${row.tool}\` | ${row.mode} |`)
  .join('\n');

const markdown = `# Safety Mode Inventory (Generated)

> This file is auto-generated by \`pnpm docs:safety:generate\`.
> Do not edit manually.

## Available safety modes

- \`read-only\`
- \`write-idempotent\`
- \`write-destructive\`

## API domain map

| API domain | CLI command module(s) | MCP tool module(s) |
| --- | --- | --- |
${domainRows}

## CLI module coverage

| CLI module | Available safety modes |
| --- | --- |
${cliCoverageRows}

## CLI subcommands and safety mode

| CLI module | Subcommand | Safety mode |
| --- | --- | --- |
${cliCommandRows}

## MCP module coverage

| MCP module | Available safety modes |
| --- | --- |
${mcpCoverageRows}

## MCP tools and safety mode

| MCP module | Tool short name | Safety mode |
| --- | --- | --- |
${mcpToolRows}
`;

writeFileSync(OUTPUT, markdown, 'utf8');
process.stdout.write(`Generated ${basename(OUTPUT)} with ${cliRows.length} CLI entries and ${mcpRows.length} MCP entries.\n`);
